import { execFileSync } from 'node:child_process'
import { mkdtempSync, readFileSync, readdirSync, rmSync, writeFileSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { basename, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const projectRoot = resolve(fileURLToPath(new URL('..', import.meta.url)))
const suppliedRepoPath = process.argv[2]
let repoRoot = suppliedRepoPath ? resolve(suppliedRepoPath) : ''
let temporaryRoot = ''

if (!repoRoot) {
  temporaryRoot = mkdtempSync(join(tmpdir(), 'leetcode-master-'))
  repoRoot = join(temporaryRoot, 'repository')
  execFileSync(
    'git',
    ['clone', '--depth', '1', 'https://github.com/youngyangyang04/leetcode-master.git', repoRoot],
    { stdio: 'inherit' },
  )
}

try {
  const catalogSource = readFileSync(join(projectRoot, 'src/catalog/problems.ts'), 'utf8')
  const definitions = Array.from(
    catalogSource.matchAll(/^\s*\[(\d+),\s*'([^']+)',\s*'([^']+)'/gm),
    (match) => ({ id: Number(match[1]), title: match[2], slug: match[3] }),
  )

  const problemDirectory = join(repoRoot, 'problems')
  const markdownFiles = readdirSync(problemDirectory, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith('.md'))
    .map((entry) => {
      const fullPath = join(problemDirectory, entry.name)
      return {
        baseName: basename(entry.name, '.md'),
        fullPath,
        text: readFileSync(fullPath, 'utf8'),
      }
    })

  const hasCpp = (text) => /^```\s*(?:cpp|c\+\+)/im.test(text)
  const countCppBlocks = (text) =>
    Array.from(text.matchAll(/^```\s*(?:cpp|c\+\+)\s*\r?\n[\s\S]*?^```/gim)).length
  const escapeRegExp = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  const embeddedSection = (text, definition) => {
    const heading = new RegExp(
      `^(#{1,4})\\s*${definition.id}\\.\\s*${escapeRegExp(definition.title)}\\s*$`,
      'm',
    ).exec(text)
    if (!heading) return ''

    const sectionStart = heading.index
    const afterHeading = text.slice(sectionStart + heading[0].length)
    const nextHeading = /^#{1,4}\s*\d+\.\s*/m.exec(afterHeading)
    const sectionEnd = nextHeading
      ? sectionStart + heading[0].length + nextHeading.index
      : text.length
    return text.slice(sectionStart, sectionEnd)
  }

  const references = {}

  for (const definition of definitions) {
    const prefix = String(definition.id).padStart(4, '0')
    const exactMatches = markdownFiles.filter((file) =>
      new RegExp(`^${prefix}(?:\\.|$)`).test(file.baseName),
    )
    let article = exactMatches.find((file) => hasCpp(file.text))
    let referenceText = article?.text ?? ''
    let pageTitle = article?.baseName.replace(/^\d{4}\./, '') ?? ''

    if (!article) {
      for (const alias of exactMatches) {
        const linkedPages = Array.from(
          alias.text.matchAll(/https:\/\/(?:www\.)?programmercarl\.com\/([^\s)]+?)\.html/g),
          (match) => decodeURIComponent(match[1]),
        )
        article = markdownFiles.find(
          (file) => linkedPages.includes(file.baseName) && hasCpp(file.text),
        )
        if (article) {
          referenceText = article.text
          pageTitle = article.baseName.replace(/^\d{4}\./, '')
          break
        }
      }
    }

    if (!article && exactMatches.length === 0) {
      for (const candidate of markdownFiles) {
        const section = embeddedSection(candidate.text, definition)
        if (section && hasCpp(section)) {
          article = candidate
          referenceText = section
          pageTitle = definition.title
          break
        }
      }
    }

    if (!article) continue

    references[definition.id] = {
      pageTitle,
      url: `https://programmercarl.com/${encodeURIComponent(article.baseName)}.html`,
      cppVariants: countCppBlocks(referenceText),
    }
  }

  const output = `// This file is generated by scripts/import-carl-references.mjs.\n` +
    `// It stores attribution metadata only; displayed C++ remains an independently normalized implementation.\n\n` +
    `export interface CarlReference {\n` +
    `  pageTitle: string\n` +
    `  url: string\n` +
    `  cppVariants: number\n` +
    `}\n\n` +
    `export const carlReferences: Readonly<Record<number, CarlReference>> = ${JSON.stringify(references, null, 2)}\n`

  writeFileSync(join(projectRoot, 'src/catalog/carlReferences.ts'), output, 'utf8')
  console.log(`Imported ${Object.keys(references).length} CodeThought references.`)
} finally {
  if (temporaryRoot) rmSync(temporaryRoot, { recursive: true, force: true })
}
